 style="font-weight: bold;" class="underline">separate categories</span>
for <br>
  </li>
  <ul>
    <li class="step c12">research <br>
    </li>
    <li class="step c12">tests<br>
    </li>
    <li class="step c12">design</li>
    <li class="step c12">troubleshooting <br>
    </li>
Pull your notes from
previous projects and summarize the process and outcomes of those
projects and how they relate to the new project.
  </ul>
  <li class="step c12"> <span style="font-weight: bold;" class="bold">Write
