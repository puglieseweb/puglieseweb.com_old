<h3 class="western c11" lang="en-GB"> What do
Commercial banks do?</h3>
<p>A <span style="font-weight: bold;" class="gergon">commercial bank</span>
<span style="font-weight: bold;" class="underline">takes deposits for
checking and savings accounts from consumers</span> while an investment
bank does not.</p>
<p>Into a commercial bank we<span style="font-weight: bold;"
 class="gergon"> deposit</span> money, and the bank <span
 style="font-weight: bold;" class="gergon">loans</span> that money to
consumers and companies in need of capital (cash).<br>
</p>
<p>The commercial bank generates a <span style="font-weight: bold;"
 class="gergon">profit</span> by paying <span
 style="font-weight: bold;" class="gergon">depositors</span> a lower
<span style="font-weight: bold;" class="gergon">interest rate</span>
than the bank charges on loans.<br>
</p>
<p>Loans from commercial banks are structured as private legally
binding contracts between two parties - the bank and you (or the bank
and a company)</p>
<br>
<h3 style="margin: 15px 0px;">Investment banks (I-bank)?<br>
</h3>
<p style="margin: 15px 0px;">An <span style="font-weight: bold;"
 class="gergon">investment bank</span> does not have an
inventory of <span style="font-weight: bold;" class="gergon">cash
deposits</span> to lend as a commercial bank does. In
essence, an investment bank acts as <span style="font-weight: bold;"
 class="underline">an intermediary, and matches sellers of <a
 href="http://en.wikipedia.org/wiki/Stock">stocks</a> and
bonds with buyers of stocks and bonds</span>.<br>
</p>
<p style="margin: 15px 0px;">&nbsp;If a company needs capital, it may
get a loan from a bank, or it may ask an investment bank to sell equity
or debt (stocks or bonds)<br>
</p>
<p>I-bank must spend considerable time finding investors in order to
obtain capital for its client. (Note that as investment banks are
increasingly seeking to become "one-stop" financing sources, many
I-banks have set aside billions of dollars of their own capital that
they can use to loan to clients directly.)</p>
