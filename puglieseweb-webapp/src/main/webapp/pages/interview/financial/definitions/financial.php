<h3>Derivative<br>
</h3>
<p>Derivative is an agreement between two parties that is
contingent on a future outcome of the underlying.
</p>
<p>A derivative is a<span style="font-weight: bold;" class="gergon">financial
instrument
</span>- or more simply, an<em> agreement between
two people
or two parties</em> - that has a value <strong>determined by the price
