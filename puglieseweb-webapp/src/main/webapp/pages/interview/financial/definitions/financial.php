<h3>Securities</h3>
<p>A security is generally a fungible, negotiable <span
 style="font-weight: bold;" class="gergon">financial instrument</span>
representing <span style="font-weight: bold;" class="gergon">financial
value</span>.</p>
<p>Securities are broadly categorized into</p>
<ul>
  <li> <span style="font-weight: bold;" class="gergon">debt securities</span>
(such as <strong>banknotes, </strong><strong>bonds</strong> and <strong>debentures</strong>)
and


    <br>
  </li>
  <li><span style="font-weight: bold;" class="gergon">equity</span>
securities, e.g., common <span style="font-weight: bold;"
 class="gergon">stocks; </span>and <span style="font-weight: bold;"
 class="gergon">derivative contracts</span>, such as forwards, futures,
options and swaps. The company or other entity issuing the security is
called the <span style="font-weight: bold;" class="gergon">issuer</span>.
A
country's
regulatory
structure determines what qualifies as a
security.</li>
</ul>
<p>Securities may be represented by a <span style="font-weight: bold;"
 class="gergon">certificate</span> or, more typically,
"non-certificated", that is in electronic or "book entry" only form.
Certificates may be bearer, meaning they entitle the holder to rights
under the security merely by holding the security, or registered,
meaning they entitle the holder to rights only if he or she appears on
a security register maintained by the issuer or an intermediary. They
include shares of corporate stock or mutual funds, bonds issued by
corporations or governmental agencies, stock options or other options,
limited partnership units, and various other formal investment
instruments that are negotiable and fungible.</p>
<h3>Bond</h3>
<p>In&nbsp;<a href="http://en.wikipedia.org/wiki/Finance"
 title="Finance"
 style="text-decoration: none; color: rgb(6, 69, 173); background-image: none;">finance</a>,
a&nbsp;bond&nbsp;is
a
debt&nbsp;<a href="http://en.wikipedia.org/wiki/Security_%28finance%29"
 title="Security (finance)"
 style="text-decoration: none; color: rgb(6, 69, 173); background-image: none;">security</a>,
in
which
the
authorized issuer owes the holders a debt and, depending
on the terms of the bond, is obliged to pay<a
 href="http://en.wikipedia.org/wiki/Interest" title="Interest"
 style="text-decoration: none; color: rgb(6, 69, 173); background-image: none;">interest</a>&nbsp;(the&nbsp;<a
 href="http://en.wikipedia.org/wiki/Coupon_%28bond%29"
 title="Coupon (bond)"
 style="text-decoration: none; color: rgb(6, 69, 173); background-image: none;">coupon</a>)
to
use
and/or
to repay the principal at a later date, termed&nbsp;<a
 href="http://en.wikipedia.org/wiki/Maturity_%28finance%29"
 title="Maturity (finance)"
 style="text-decoration: none; color: rgb(6, 69, 173); background-image: none;">maturity</a>.
<br>
</p>
<p>A bond is a formal contract to repay borrowed money with interest at
fixed intervals</p>
<p>Thus a bond is like a loan: the issuer is the borrower (debtor), the
holder is the lender (creditor), and the coupon is the interest. Bonds
provide the borrower with external funds to finance long-term
investments, or, in the case of government bonds, to finance current
expenditure. Certificates of deposit (CDs) or commercial paper are
considered to be money market instruments and not bonds.</p>
<p>Bonds and stocks are both securities, but the major difference
between the two is that (capital) stockholders have an equity stake in
the company (i.e., they are owners), whereas bondholders have a
creditor stake in the company (i.e., they are lenders). Another
difference is that bonds usually have a defined term, or maturity,
after which the bond is redeemed, whereas stocks may be outstanding
indefinitely. An exception is a consol bond, which is a perpetuity
(i.e., bond with no maturity).</p>
